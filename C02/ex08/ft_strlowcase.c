char	*ft_strlowcase(char *str)
{
	int gap;
	int len;

	gap = 'A' - 'a';
	len = 0;
	while (*(str + len))
	{
		if ( *(str + len) >= 'A' && *(str + len) <= 'Z')
			*(str + len) -= gap;
		len++;
	}
	return str;
}
