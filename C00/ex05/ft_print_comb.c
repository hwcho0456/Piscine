#include <unistd.h>

void	ft_print_comb(void)
{
	char word[5];

	word[0] = '0' - 1;
	word[3] = ',';
	word[4] = ' ';
	while (++word[0] <= '6')
	{
		word[1] = word[0];
		while (++word[1] <= '8')
		{
			word[2] = word[1];
			while (++word[2] <= '9')
			{
				write(1, word, 5);
			}
		}
	}
	write(1, "789", 3);
}
