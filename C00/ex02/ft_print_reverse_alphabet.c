void ft_print_reverse_alphabet(void)
{
	char z = 'z';
	char c = 'z';
	int cnt = 0;
	
	while(cnt<25)
	{
		write(1,&c,1);
		cnt++;
		c=z-cnt;
	}
	
	return 0;
}
