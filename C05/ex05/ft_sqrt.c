int	ft_sqrt(int nb)
{
	int square;
	int a;

	if (nb <= 0)
		return (0);
	square = 1;
	a = 1;
	while (square <= nb)
	{
		if (square == nb)
			return (a);
		a++;
		square = a * a;
	}
	return (0);
}
