char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int len;

	len = 0;
	while (*(dest + len))
		len++;
	while (*(src) || nb-- > 0)
		*(dest + len++) = *(src++);
	*(dest + len) = '\0';
	return (dest);
}
