int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int len;

	if (n == 0)
		return (0);
	len = 0;
	while ((*(s1 + len) == *(s2 + len)) && *(s1 + len) && *(s2 + len))
	{
		if (len == n - 1)
			return (*(s1 + len) - *(s2 + len));
		len++;
	}
	return (*(s1 + len) - *(s2 + len));
}
