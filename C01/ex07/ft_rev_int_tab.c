void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int temp;

	i = -1;
	j = size;
	while (++i < --j)
	{
		temp = tab[i];
		tab[i] = tab[j];
		tab[j] = temp;
	}
}
